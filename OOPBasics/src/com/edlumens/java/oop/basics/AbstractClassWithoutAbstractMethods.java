package com.edlumens.java.oop.basics;

//AbstractClassWithoutAbstractMethods.java
public abstract class AbstractClassWithoutAbstractMethods {

	// Non-abstract method
	public void displayMessage() {
		System.out.println("This is a message from the abstract class.");
	}

	// Another non-abstract method
	public int add(int a, int b) {
		return a + b;
	}

 
 
}
